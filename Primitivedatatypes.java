public class Primitivedatatypes {
    public static void main(String args[]) {
        /*
         * primitive datatypes always have a value.
         * byte
         * short
         * long
         * int
         * float
         * double
         * char
         * boolen
         */
        int a = 10;
        double f = 2.5;
        char c = 'S';
        // boolean b = 8431;
        System.out.println("a=" + a + "\nf=" + f + "\nc=" + c);
    }

}
